package mypackage;

public class UnaryOperators {

	public static void main(String[] args) {
		int a=10;
		int b=15;
		//System.out.println(c);
		//a++;
		int c=a+b;
		System.out.println(c);
		++a;
		System.out.println(a);
		--b;
		System.out.println(b);
		int d=a+b;
		System.out.println(d);
		
	}

}
